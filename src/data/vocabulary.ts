export function getVocabByLevel(level: HSKLevel): VocabEntry[] {
  return TOCFL_VOCAB.filter((v) => v.level === level);
}

export function getVocabByTag(tag: string): VocabEntry[] {
  return TOCFL_VOCAB.filter((v) => v.tags.includes(tag));
}

export function getRandomVocab(
  level: HSKLevel | "all",
  count: number
): VocabEntry[] {
  const pool =
    level === "all" ? TOCFL_VOCAB : TOCFL_VOCAB.filter((v) => v.level === level);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

